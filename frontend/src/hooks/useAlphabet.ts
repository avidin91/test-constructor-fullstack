export const useAlphabet = (id: number ) => {
    if (id === 1) {
        return 'а)'
    }
    if (id === 2) {
        return 'б)'
    }
    if (id === 3) {
        return 'в)'
    }
    if (id === 4) {
        return 'г)'
    }
    if (id === 5) {
        return 'д)'
    }
    if (id === 6) {
        return 'е)'
    }
    if (id === 7) {
        return 'ж)'
    }
    if (id === 8) {
        return 'з)'
    }
}